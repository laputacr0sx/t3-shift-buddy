export function getSliceRota(rotaArr: string[]): Rota {
    const result = [];
    let row = [];
    for (let i = 0; i < rotaArr.length; i++) {
        const duty = rotaArr[i] as string;
        row.push(duty);
        if (i % 7 === 6) {
            result.push(row);
            row = [];
        }
    }
    return result;
}

